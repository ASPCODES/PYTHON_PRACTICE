# Q5. Take input from the user as a string for a number, and convert it to an integer, then add 10 to it and print the result.

# Take input from the user

num_str = input("Enter a number: ")

# Convert the string input to an integer
num_int = int(num_str)

# Add 10 to the integer
result = 10 + num_int

print("Result after adding 10:", result)